    public void onLogin(View view) {
        setContentView(R.layout.main);
        BottomNavigationView navView = findViewById(R.id.nav_view);

        AppBarConfiguration appBarConfiguration = new AppBarConfiguration.Builder(
                R.id.navigation_home, R.id.navigation_add)
                .build();
        NavController navController = Navigation.findNavController(this, R.id.nav_host_fragment);
        NavigationUI.setupActionBarWithNavController(this, navController, appBarConfiguration);
        NavigationUI.setupWithNavController(navView, navController);
    }

    public void saveToDB(View view) {

        SQLiteDatabase db = new DBSQLiteHelper(view.getContext()).getWritableDatabase();

        ContentValues values = new ContentValues();
        String desc = ((EditText) findViewById(R.id.descLabel)).getText().toString();

        values.put("description", desc);
        values.put("date", new SimpleDateFormat("dd.MM.yyyy").format(new Date()));

        db.insert(DB.Deviation.TABLE_NAME, null, values);
        db.close();

//        Fragment fragment = new HomeFragment();
    }

